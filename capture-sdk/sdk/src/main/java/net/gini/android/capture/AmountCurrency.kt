package net.gini.android.capture

/**
 * Enum class represents currently supported currencies
 *
 * <p>
 * Currency codes expressed in ISO 4217 standard </p>
 *
 * <ul>
 *  <li> EUR - stands for EURO (official currency of the euro-zone)
 *  <li> GBP - stands for the British pound sterling
 *  <li> USD - stands for United States Dollar
 *  <li> CHF - stands for Confoederatio Helvetica franc
 * </ul>
 */
enum class AmountCurrency {
    EUR, GBP, USD, CHF
}